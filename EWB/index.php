    <h3>What is Engineers Without Borders USA?</h3>
      <p>Engineers Without Borders USA (EWB-USA) is a non-profit humanitarian organization dedicated to partnering with developing communities around the world. We provide engineering services through our volunteer chapter members to implement the infrastructure necessary to meet basic human needs. Through our sustainable engineering projects, we strive to improve the quality of life in the communities we serve. Thousands of members in more than 225 university and professional chapters work to support these efforts.</p>
      
      <p>Learn more about EWB-USA at the national website, <a href="http://www.ewb-usa.org">ewb-usa.org</a>.</p>

    <h4>EWB-USA History</h4>
      <p>EWB-USA was founded in 2002 by University of Colorado-Boulder Civil Engineering professor, Dr. Bernard Amadei in order to address some of the most prevalent challenges facing the developing world, including water purification and delivery, sanitation, transportation and infrastructure projects, and sustainable and renewable energy systems. What started as a backyard conversation with a landscaper from San Pablo, Belize resulted in a clean water system for the local community and sparked a global engineering movement that has directly impacted more than one million people to date.</p>
      
      <p>The nonprofit started with 18 members and one university chapter. Today, EWB-USA is comprised of an expansive network of over 12,000 members and more than 225 university student and professional chapters engaged in over 400 active programs in 45 countries around the world.</p>

    <h4>EWB-USA Vision and Mission</h4>
      <p>Our Vision is a world in which the communities we serve have the capacity to sustainably meet their basic human needs, and that our members have enriched global perspectives through the innovative professional educational opportunities that the EWB-USA program provides.</p>

      <p>EWB-USA supports community-driven development programs worldwide by collaborating with local partners to design and implement sustainable engineering projects, while creating transformative experiences and responsible leaders.</p>
      
